    public function availability(Request $request){

        if($request->ajax())
        {

            $city = City::find($request->id);

            if($city->is_available === 1){

                $city->is_available= 0;

            }

            else {

                $city->is_available = 1;

            }

            $city->save();

            return 'success';

        }

    }


